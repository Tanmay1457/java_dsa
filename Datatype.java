public class Datatype {
    // Primitive datatype in java

    public static void main(String args[]) {
        byte b = 8; // 1] Byte datatype(range 256 byte)...........................>
        System.out.println(b); 

        char ch = 'a'; // 2] Character datatype...................................>
        System.out.println(ch);

        boolean var = true; // 3] Boolean datatype(true or false)..................>
        System.out.println(var);

        float price = 10.5f; // 4] float datatype(last we have to mention it as "f")...........> 
        System.out.println(price);

        // 5] for long value of decimal number(i.e float)we use "double" datatype..............>

        int number = 24; // 6] Integer datatype.............................................>
        System.out.println(number);

        // 7] for long integer value we use "long" datatype..................................>
    }
}
